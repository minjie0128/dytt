package com.alading.ee.vo;

public class ScanLog {
	private String mUniqueId;
	private String mMobile;
	private String mNumber;
	private float mAmount;
	private String mLogTime;
	private int mLogResult;
	private String mUserName;
	private String mUdid;
	private int mLogStatus;
	private int mUploadStatus;

	public String getmUniqueId() {
		return mUniqueId;
	}

	public void setmUniqueId(String mUniqueId) {
		this.mUniqueId = mUniqueId;
	}

	public String getmMobile() {
		return mMobile;
	}

	public void setmMobile(String mMobile) {
		this.mMobile = mMobile;
	}

	public String getmNumber() {
		return mNumber;
	}

	public void setmNumber(String mNumber) {
		this.mNumber = mNumber;
	}

	public float getmAmount() {
		return mAmount;
	}

	public void setmAmount(float mAmount) {
		this.mAmount = mAmount;
	}

	public String getmLogTime() {
		return mLogTime;
	}

	public void setmLogTime(String mLogTime) {
		this.mLogTime = mLogTime;
	}

	public int getmLogResult() {
		return mLogResult;
	}

	public void setmLogResult(int mLogResult) {
		this.mLogResult = mLogResult;
	}

	public String getmUserName() {
		return mUserName;
	}

	public void setmUserName(String mUserName) {
		this.mUserName = mUserName;
	}

	public String getmUdid() {
		return mUdid;
	}

	public void setmUdid(String mUdid) {
		this.mUdid = mUdid;
	}

	public int getmLogStatus() {
		return mLogStatus;
	}

	public void setmLogStatus(int mLogStatus) {
		this.mLogStatus = mLogStatus;
	}

	public int getmUploadStatus() {
		return mUploadStatus;
	}

	public void setmUploadStatus(int mUploadStatus) {
		this.mUploadStatus = mUploadStatus;
	}
}
